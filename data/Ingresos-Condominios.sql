WITH IngresosCondominios AS (
  SELECT 
      IDINGRESO,
      IDCLIENTE,
      DESARROLLO,
      UNIDAD,
      CONCAT("STP_", STP) AS  STP,
      FOLIO,
      FECHA_INGRESO,
      FECHA_REGISTRO,
      CLIENTE,
      CAST(MONTO_PAGADO AS FLOAT64) AS MONTO_PAGADO,
      BANCO,
      NUMERO_CUENTA,	
      FORMA_PAGO,	
      USUARIOS_REGISTRO,	
      STATUS,	
      SISTEMA,	
      CAST(SALDOPENDIENTE_POR_APLICAR AS FLOAT64) AS SALDO_PENDIENTE_POR_APLICAR,	
      id_ingreso_dt,	
      CAST(MONTO_CUOTA AS FLOAT64) AS MONTO_CUOTA,	
      CAST(MONTO_RESERVA AS FLOAT64) AS MONTO_RESERVA,	
      CAST(MONTO_FONDO AS FLOAT64) AS MONTO_FONDO
  FROM `terraviva-439415.sheets_condominios.sheets_flujo_ingresos`
)
SELECT * FROM IngresosCondominios
WHERE EXTRACT(YEAR FROM FECHA_INGRESO) = EXTRACT(YEAR FROM CURRENT_DATE())
  AND EXTRACT(MONTH FROM FECHA_INGRESO) = EXTRACT(MONTH FROM CURRENT_DATE())